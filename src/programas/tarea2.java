
package programas;
import java.util.Scanner;
public class tarea2 {
    public static void main(String[] args){
        int suma = 0;
        
        // Estructura repetitiva for
        for (int i = 1; i <= 10; i++) {
            suma += i; // suma = suma + i
        }
        
        System.out.println("La suma de los primeros 10 numeros es: " + suma);
    }
}
